export function highlightCodeBlocks(root: ParentNode): void {
  root.querySelectorAll<HTMLElement>("pre code[class*='language-']").forEach((code) => {
    if (code.dataset.highlighted === "yes") return;
    hljs.highlightElement(code);
  });
}
